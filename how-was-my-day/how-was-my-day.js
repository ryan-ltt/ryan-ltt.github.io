const COLORS = ['#d32f2f','#e64a19','#f57c00','#fbc02d','#388e3c','#1976d2','#7b1fa2'];

let mode = 'week';
let cursor = null;

function isoDate(d) {
	return d.toISOString().slice(0, 10);
}

function addDays(d, n) {
	const r = new Date(d);
	r.setDate(r.getDate() + n);
	return r;
}

function startOfWeek(d) {
	const r = new Date(d);
	r.setDate(r.getDate() - r.getDay());
	return r;
}

function startOfMonth(d) {
	return new Date(d.getFullYear(), d.getMonth(), 1);
}

function startOfYear(d) {
	return new Date(d.getFullYear(), 0, 1);
}

function allDates() {
	return [...data.keys()].sort();
}

function lastDate() {
	const keys = allDates();
	return new Date(keys[keys.length - 1] + 'T00:00:00');
}

function initCursor() {
	const d = lastDate();
	if (mode === 'week') cursor = startOfWeek(d);
	else if (mode === 'month') cursor = startOfMonth(d);
	else cursor = startOfYear(d);
}

function getWindow() {
	if (mode === 'week') {
		const start = new Date(cursor);
		const end = addDays(start, 6);
		return { start, end };
	}
	if (mode === 'month') {
		const start = new Date(cursor);
		const end = new Date(start.getFullYear(), start.getMonth() + 1, 0);
		return { start, end };
	}
	const start = new Date(cursor.getFullYear(), 0, 1);
	const end = new Date(cursor.getFullYear(), 11, 31);
	return { start, end };
}

function moveWindow(dir) {
	if (mode === 'week') cursor = addDays(cursor, dir * 7);
	else if (mode === 'month') cursor = new Date(cursor.getFullYear(), cursor.getMonth() + dir, 1);
	else cursor = new Date(cursor.getFullYear() + dir, 0, 1);
}

function formatLabel() {
	const { start, end } = getWindow();
	const months = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
	if (mode === 'week') {
		return `${months[start.getMonth()]} ${start.getDate()} – ${months[end.getMonth()]} ${end.getDate()}, ${end.getFullYear()}`;
	}
	if (mode === 'month') {
		return `${months[start.getMonth()]} ${start.getFullYear()}`;
	}
	return `${start.getFullYear()}`;
}

function datesInRange(start, end) {
	const out = [];
	let d = new Date(start);
	while (d <= end) {
		out.push(new Date(d));
		d = addDays(d, 1);
	}
	return out;
}

function colorForRating(r) {
	return COLORS[Math.max(0, Math.min(6, r - 1))];
}

function avg(vals) {
	if (!vals.length) return null;
	return vals.reduce((a, b) => a + b, 0) / vals.length;
}

function render() {
	const { start, end } = getWindow();
	document.getElementById('period-label').textContent = formatLabel();

	const dates = datesInRange(start, end);
	const labels = [];
	const values = [];
	const colors = [];

	if (mode === 'year') {
		const months = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
		for (let m = 0; m < 12; m++) {
			labels.push(months[m]);
			const vals = [];
			let d = new Date(start.getFullYear(), m, 1);
			while (d.getMonth() === m) {
				const v = data.get(isoDate(d));
				if (v !== undefined) vals.push(v);
				d = addDays(d, 1);
			}
			const a = avg(vals);
			values.push(a);
			colors.push(a !== null ? colorForRating(Math.round(a)) : '#ccc');
		}
	} else {
		for (const d of dates) {
			const key = isoDate(d);
			const v = data.get(key) ?? null;
			if (mode === 'week') {
				const days = ['Sun','Mon','Tue','Wed','Thu','Fri','Sat'];
				labels.push(days[d.getDay()]);
			} else {
				labels.push(d.getDate().toString());
			}
			values.push(v);
			colors.push(v !== null ? colorForRating(v) : '#ddd');
		}
	}

	drawChart(labels, values, colors);
	renderStats(values.filter(v => v !== null));
}

function drawChart(labels, values, colors) {
	const canvas = document.getElementById('chart');
	const dpr = window.devicePixelRatio || 1;
	const W = canvas.parentElement.clientWidth - 44;
	const H = Math.max(220, Math.round(W * 0.28));
	canvas.width = W * dpr;
	canvas.height = H * dpr;
	canvas.style.width = W + 'px';
	canvas.style.height = H + 'px';

	const ctx = canvas.getContext('2d');
	ctx.scale(dpr, dpr);

	const padL = 36, padR = 10, padT = 14, padB = 28;
	const chartW = W - padL - padR;
	const chartH = H - padT - padB;
	const minR = mode === 'year' ? 3 : 1;
	const maxR = mode === 'year' ? 6 : 7;

	ctx.clearRect(0, 0, W, H);

	ctx.strokeStyle = '#ddd';
	ctx.lineWidth = 1;
	ctx.font = '11px Monaco';
	ctx.fillStyle = '#999';
	for (let r = minR; r <= maxR; r++) {
		const y = padT + chartH - ((r - minR) / (maxR - minR)) * chartH;
		ctx.beginPath();
		ctx.moveTo(padL, y);
		ctx.lineTo(padL + chartW, y);
		ctx.stroke();
		ctx.fillText(r, 4, y + 4);
	}

	const n = labels.length;
	const barW = Math.max(2, chartW / n - 2);
	const gap = chartW / n;

	for (let i = 0; i < n; i++) {
		const x = padL + i * gap + gap / 2 - barW / 2;
		const v = values[i];
		if (v === null) continue;
		const barH = ((v - minR) / (maxR - minR)) * chartH;
		const y = padT + chartH - barH;
		ctx.fillStyle = colors[i];
		ctx.fillRect(x, y, barW, barH);
	}

	ctx.fillStyle = '#555';
	ctx.font = '10px Monaco';
	ctx.textAlign = 'center';
	const step = n <= 31 ? 1 : Math.ceil(n / 12);
	for (let i = 0; i < n; i += step) {
		const x = padL + i * gap + gap / 2;
		ctx.fillText(labels[i], x, H - padB + 14);
	}
}

function renderStats(vals) {
	if (!vals.length) { document.getElementById('stats').innerHTML = ''; return; }
	const a = avg(vals).toFixed(2);
	const mn = Math.min(...vals);
	const mx = Math.max(...vals);
	document.getElementById('stats').innerHTML = `
		<div class="stat-box"><div class="stat-label">average</div><div class="stat-value">${a}</div></div>
		<div class="stat-box"><div class="stat-label">low</div><div class="stat-value">${mn}</div></div>
		<div class="stat-box"><div class="stat-label">high</div><div class="stat-value">${mx}</div></div>
		<div class="stat-box"><div class="stat-label">days</div><div class="stat-value">${vals.length}</div></div>
	`;
}

