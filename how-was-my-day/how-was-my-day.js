function buildCalTable() {
	const months = ['jan','feb','mar','apr','may','jun','jul','aug','sep','oct','nov','dec'];
	const keys = allDates();
	const firstDate = new Date(keys[0] + 'T00:00:00');
	const lastD = new Date(keys[keys.length - 1] + 'T00:00:00');

	const startYear = firstDate.getFullYear();
	const endYear = lastD.getFullYear();

	// build list of (year, month) columns
	const cols = [];
	for (let y = startYear; y <= endYear; y++) {
		for (let m = 0; m < 12; m++) {
			if (y === startYear && m < firstDate.getMonth()) continue;
			if (y === endYear && m > lastD.getMonth()) continue;
			cols.push({ y, m });
		}
	}

	const table = document.getElementById('cal-table');
	table.innerHTML = '';

	// year header row
	const thead = table.createTHead();
	const yearRow = thead.insertRow();
	const cornerY = document.createElement('th');
	cornerY.className = 'year-header';
	cornerY.textContent = '';
	yearRow.appendChild(cornerY);

	let ci = 0;
	while (ci < cols.length) {
		const y = cols[ci].y;
		let span = 0;
		while (ci + span < cols.length && cols[ci + span].y === y) span++;
		const th = document.createElement('th');
		th.className = 'year-header';
		th.colSpan = span;
		th.textContent = y;
		yearRow.appendChild(th);
		ci += span;
	}

	// month header row
	const monthRow = thead.insertRow();
	const cornerM = document.createElement('th');
	cornerM.className = 'month-header';
	cornerM.textContent = 'day';
	monthRow.appendChild(cornerM);
	for (const { m } of cols) {
		const th = document.createElement('th');
		th.className = 'month-header';
		th.textContent = months[m];
		monthRow.appendChild(th);
	}

	// day rows 1–31
	const tbody = table.createTBody();
	for (let day = 1; day <= 31; day++) {
		const tr = tbody.insertRow();
		const rowHeader = tr.insertCell();
		rowHeader.className = 'row-header';
		rowHeader.textContent = day;

		for (const { y, m } of cols) {
			const td = tr.insertCell();
			const daysInMonth = new Date(y, m + 1, 0).getDate();
			if (day > daysInMonth) {
				td.className = 'empty';
				td.textContent = '';
			} else {
				const key = `${y}-${String(m + 1).padStart(2,'0')}-${String(day).padStart(2,'0')}`;
				const v = data.get(key);
				if (v === undefined) {
					td.className = 'empty';
					td.textContent = 'n/a';
				} else {
					td.textContent = v;
					td.style.backgroundColor = colorForRating(v);
					td.style.color = v >= 5 ? '#fff' : '#111';
				}
			}
		}
	}

	// AVG row
	const avgRow = tbody.insertRow();
	const avgHeader = avgRow.insertCell();
	avgHeader.className = 'row-header avg-row';
	avgHeader.textContent = 'AVG';
	for (const { y, m } of cols) {
		const td = avgRow.insertCell();
		td.className = 'avg-row';
		const vals = [];
		const daysInMonth = new Date(y, m + 1, 0).getDate();
		for (let d = 1; d <= daysInMonth; d++) {
			const key = `${y}-${String(m + 1).padStart(2,'0')}-${String(d).padStart(2,'0')}`;
			const v = data.get(key);
			if (v !== undefined) vals.push(v);
		}
		if (vals.length) {
			const a = avg(vals);
			td.textContent = a.toFixed(2);
			td.style.backgroundColor = colorForRating(Math.round(a));
			td.style.color = Math.round(a) >= 5 ? '#fff' : '#111';
		} else {
			td.textContent = '';
		}
	}
}

